package Ternary_Operators;

/**
 * 
 * @author Flexer
 * 
 * They are simply a condition based statement.
 * If true, then assign this, else, assign that.
 * 
 * Example below!
 */

public class ex1_Tutorial {
	
	public static void main(String[] args) {
		
		int a, b;
		a=1;
		b=0;
		
		int largerNum = (a > b) ? a : b;
		
		System.out.println(largerNum);
		
	}

}
