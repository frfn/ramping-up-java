package chapter2ProgrammingProjects;

//Application that reads three integers and returns the average

public class TwoPoint2 {

	public static void main(String[] average) {
		
		int num = 10, num2 = 20, num3 = 30, total;
		double avg;
		
		total = num + num2 + num3;
		
		avg = total / 3;
		
		System.out.print("The average of three numbers, 10, 20, 30 is: " + avg);
		
	}
	
}
